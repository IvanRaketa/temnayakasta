export function getLegalOperatorInfo() {
  return {
    name: readEnv("LEGAL_OPERATOR_NAME") ?? LEGAL_OPERATOR_FALLBACK,
    type: LEGAL_OPERATOR_TYPE,
    email: readEnv("LEGAL_OPERATOR_EMAIL") ?? LEGAL_OPERATOR_FALLBACK,
    phone: readEnv("LEGAL_OPERATOR_PHONE") ?? LEGAL_OPERATOR_FALLBACK,
    address: readEnv("LEGAL_OPERATOR_ADDRESS") ?? LEGAL_OPERATOR_FALLBACK,
    rknNumber: readEnv("LEGAL_RKN_NUMBER"),
  };
}

export function hasLegalOperatorInfo() {
  const info = getLegalOperatorInfo();
  return Boolean(
    info.name !== LEGAL_OPERATOR_FALLBACK ||
      info.email !== LEGAL_OPERATOR_FALLBACK ||
      info.address !== LEGAL_OPERATOR_FALLBACK ||
      info.phone !== LEGAL_OPERATOR_FALLBACK ||
      info.rknNumber,
  );
}
